# 위에서부터 순서대로 첫 번째 매칭에서 반환

_RULES: list[tuple[re.Pattern, str | None]] = []


def _r(pattern: str, replacement: str | None) -> None:
    """규칙 등록 헬퍼."""
    _RULES.append((re.compile(pattern), replacement))


