# ── 변환 함수 ────────────────────────────────────────────────────────────────

def humanize(msg: str) -> str | None:
    """파이프라인 로그 메시지를 사용자 친화적 형태로 변환.

    Args:
        msg: 원본 로그 메시지

    Returns:
        변환된 메시지. None이면 화면에 표시하지 않음(내부 디버그 정보).
        매칭 규칙이 없으면 원본 메시지를 그대로 반환.
    """
    stripped = msg.strip()
    for pattern, replacement in _RULES:
        m = pattern.search(stripped)
        if m:
            if replacement is None:
                return None
            # m.expand()로 역참조(\1, \2...)만 치환한 교체 문자열 반환
            # re.sub()와 달리 원본에서 미매칭 부분이 남지 않음
            # rstrip()만 사용: 교체 문자열의 앞쪽 들여쓰기 공백을 보존
            return m.expand(replacement).rstrip()
    return stripped
